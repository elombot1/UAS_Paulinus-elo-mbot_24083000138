// Class Matematika
public class Matematika {

    // Overloaded Method pertambahan
    public int pertambahan(int a, int b) {
        return a + b;
    }

    public int pertambahan(int a, int b, int c) {
        return a + b + c;
    }

    public double pertambahan(double a, double b) {
        return a + b;
    }

    public double pertambahan(double a, double b, double c) {
        return a + b + c;
    }

    // Overloaded Method pengurangan
    public int pengurangan(int a, int b) {
        return a - b;
    }

    public double pengurangan(double a, double b) {
        return a - b;
    }

    // Overloaded Method perkalian
    public int perkalian(int a, int b) {
        return a * b;
    }

    public double perkalian(double a, double b) {
        return a * b;
    }

    // Overloaded Method pembagian
    public int pembagian(int a, int b) {
        return a / b;
    }

    public double pembagian(double a, double b) {
        return a / b;
    }

    // Overloaded Method modulus
    public int modulus(int a, int b) {
        return a % b;
    }

    public double modulus(double a, double b) {
        return a % b;
    }
}


