// Class MatematikaCanggih
public class MatematikaCanggih {

    // Method pertambahan
    public int pertambahan(int a, int b) {
        return a + b;
    }

    // Method perkalian
    public int perkalian(int a, int b) {
        return a * b;
    }

    // Method modulus
    public int modulus(int a, int b) {
        return a % b;
    }
}

